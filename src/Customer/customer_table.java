    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // Insert data
        String id=a.getText();
        String name=b.getText();
        String address=c.getText();
        String gmail=d.getText();
        String contact=e.getText();
        if(id.isEmpty()){
            JOptionPane.showMessageDialog(this, "ID Is Empty", "Empty!!", 1);
            a.requestFocus();
        }
        else if(name.isEmpty()){
            JOptionPane.showMessageDialog(this, "Name Is Empty", "Empty!!", 1);
            b.requestFocus();
        }
        else if(address.isEmpty()){
            JOptionPane.showMessageDialog(this, "Address Is Empty", "Empty!!", 1);
            c.requestFocus();
        }
        else if(gmail.isEmpty()){
            JOptionPane.showMessageDialog(this, "G-mail Is Empty", "Empty!!", 1);
            d.requestFocus();
        }
        else if(contact.isEmpty()){
            JOptionPane.showMessageDialog(this, "Contact Is Empty", "Empty!!", 1);
            e.requestFocus();
        }
        else{
            Con();
            try {
String sq="INSERT INTO `customer` (`ID`,`Customer Name`,`Address`,`G-mail`,`Contact`) VALUES ('"+id+"','"+name+"','"+address+"','"+gmail+"','"+contact+"')";
           int i=st.executeUpdate(sq);
           if(i>=1){
           JOptionPane.showMessageDialog(this, "Insert Successful", "Successfully!!",JOptionPane.PLAIN_MESSAGE);
           clear();
           String ss="SELECT *FROM `customer`";
           Showtable(ss);
           a.setText(""+Autoid());
           }
           else{
           JOptionPane.showMessageDialog(this, "Insert Failed", "Failed!!",JOptionPane.PLAIN_MESSAGE);
           }
            } catch (SQLException ex) {
                Logger.getLogger(customer_table.class.getName()).log(Level.SEVERE, null, ex);
            }
            
        }
    }//GEN-LAST:event_jButton2ActionPerformed


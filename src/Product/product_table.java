    int Autoid(){int at=0;
        String sq="SELECT `ID` FROM `product`";
        Con();
    try {
        rs=st.executeQuery(sq);
        while(rs.next()){
        at=rs.getInt(1);
        }
        at++;
        
        } catch (SQLException ex) {
            Logger.getLogger(product_table.class.getName()).log(Level.SEVERE, null, ex);
        }
      return at;
    }

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // Insert Data
        String id=a.getText();
        String name=b.getText();
        String price=c.getText();
        String quantity=d.getText();
        String company=e.getText();
        String details=f.getText();
        if(id.isEmpty()){
            JOptionPane.showMessageDialog(this, " ID is Empty!!");
            a.requestFocus();
        }
        else if(name.isEmpty()){
            JOptionPane.showMessageDialog(this, " Name is Empty!!");
            b.requestFocus();
        }
        else if(price.isEmpty()){
            JOptionPane.showMessageDialog(this, " Price is Empty!!");
            c.requestFocus();
        }
        else if(quantity.isEmpty()){
            JOptionPane.showMessageDialog(this, " Quantity is Empty!!");
            d.requestFocus();
        }
        else if(company.isEmpty()){
            JOptionPane.showMessageDialog(this, " Company is Empty!!");
            e.requestFocus();
        }
        else if(details.isEmpty()){
            JOptionPane.showMessageDialog(this, " Details is Empty!!");
            f.requestFocus();
        }
        else{
            try {
String sq="INSERT INTO `product` (`ID`,`Product Name`,`Price`,`Quantity or Weight`,`Company`,`Details`) VALUES ('"+id+"','"+name+"','"+price+"','"+quantity+"','"+company+"','"+details+"')";
                Con();
               int i=st.executeUpdate(sq);
                if(i>=1){
                JOptionPane.showMessageDialog(this, "Data Insert Success");
                Clear();
                String dd="SELECT * FROM `product`";
                Showtable(dd);
                a.setText(""+Autoid());
                }
                else{
                JOptionPane.showMessageDialog(this, "Data Insert Failed!!");
                }
            } catch (SQLException ex) {
                Logger.getLogger(product_table.class.getName()).log(Level.SEVERE, null, ex);
            }
        
        
        }
    }//GEN-LAST:event_jButton2ActionPerformed


    void Showtable(String dd){
        DefaultTableModel ddt=(DefaultTableModel)jTable1.getModel();
            ddt.setRowCount(0);
            Con();
            
        try {
          rs=st.executeQuery(dd);
            while(rs.next()){
            ddt.addRow(new Object[]{rs.getString(1),rs.getString(2),rs.getString(3),rs.getString(4),rs.getString(5),rs.getString(6)});
            }
        } catch (SQLException ex) {
            Logger.getLogger(product_table.class.getName()).log(Level.SEVERE, null, ex);
        }
    
    }
